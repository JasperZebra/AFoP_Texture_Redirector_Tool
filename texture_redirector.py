    def _validate_manual_name(self, old_tex):
        """Validate manual texture name length matches original."""
        if old_tex not in self._tex_manual_entries:
            return
        
        entry = self._tex_manual_entries[old_tex]
        manual_name = entry.get().strip()
        suffix = get_texture_suffix(old_tex)
        old_stem = old_tex[: -len(suffix)] if suffix else old_tex
        
        # Only validate if manual mode is active
        if not self._tex_manual_vars.get(old_tex, tk.BooleanVar(value=False)).get():
            return
        
        # Check length
        if len(manual_name) == len(old_stem):
            entry.config(fg=ACCENT2)
        elif len(manual_name) == 0:
            entry.config(fg=TEXT)  # Empty is neutral
        else:
            entry.config(fg=ERROR)
        
        self._update_tex_len_lbl()

    def _update_tex_len_lbl(self):
        """Recount length errors for currently enabled textures and update the label."""
        if not self._sel_res:
            return
        new_mg = self._new_mg_var.get().strip()
        if new_mg.lower().endswith(".mgraphobject"):
            new_mg = new_mg[: -len(".mgraphobject")]
        if not new_mg:
            self._tex_len_lbl.config(text="")
            return
        
        pairs = self._derive_texture_names(new_mg)
        err_count  = 0
        active_count = 0

        for old_t, new_t, _ in pairs:
            suffix = get_texture_suffix(old_t)
            if not suffix:  # skip non-targeted textures
                continue

            old_stem = old_t[: -len(suffix)]

            # Check if auto checkbox is enabled
            if self._tex_check_vars.get(old_t, tk.BooleanVar(value=False)).get():
                active_count += 1
                new_stem = new_t[: -len(suffix)]
                if len(new_stem) != len(old_stem):
                    err_count += 1

            # Check if manual checkbox is enabled
            elif self._tex_manual_vars.get(old_t, tk.BooleanVar(value=False)).get():
                active_count += 1
                entry = self._tex_manual_entries.get(old_t)
                if entry:
                    manual_stem = entry.get().strip()
                    if manual_stem and len(manual_stem) != len(old_stem):
                        err_count += 1

        if active_count == 0:
            self._tex_len_lbl.config(text="", fg=MUTED)
        else:
            self._tex_len_lbl.config(
                text=f"{err_count} length error(s)" if err_count else "✓ all match",
                fg=ERROR if err_count else SUCCESS)

