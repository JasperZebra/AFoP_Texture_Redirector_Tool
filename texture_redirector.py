    # ── Copy & rename ──────────────────────────────────────────────────────────

    def _copy_to_mod(self):
        item = self._sel_res
        if not item:
            messagebox.showwarning("Nothing selected",
                                "Select a file in the Resource Folder panel first.")
            return
        mod = self._mod_var.get().strip()
        if not mod:
            messagebox.showerror("No mod folder", "Set a Mod Output Folder first.")
            return

        new_mg_stem = self._new_mg_var.get().strip()
        if not new_mg_stem:
            messagebox.showerror("No name", "Enter a new graph file name.")
            return

        # Strip extension if the user typed it in (e.g. "foo.mgraphobject" -> "foo")
        if new_mg_stem.lower().endswith(".mgraphobject"):
            new_mg_stem = new_mg_stem[: -len(".mgraphobject")]

        src         = item["path"]
        old_mg_stem = src.stem
        gear_swap   = self._gear_swap_only.get()
        mod_folder  = Path(mod)

        # Build a map of old_filename -> (original_full_path, new_full_path) for patching
        texture_path_map = {}  # old_filename -> (old_full_path, new_full_path)
        
        if not gear_swap:
            # ── Derive texture rename map - handles both auto and manual naming
            tex_pairs = self._derive_texture_names(new_mg_stem)  # [(old, new), ...] for auto mode
            length_errors = []
            tex_rename_map = {}  # old_name -> new_name
            
            for o, n, _linked in tex_pairs:
                # Check if manual naming is enabled for this texture
                is_manual = self._tex_manual_vars.get(o, tk.BooleanVar(value=False)).get()
                is_auto = self._tex_check_vars.get(o, tk.BooleanVar(value=True)).get()
                
                if is_manual:
                    # Use manual name from Entry widget
                    entry = self._tex_manual_entries.get(o)
                    if not entry:
                        messagebox.showerror("Error", f"Manual entry widget not found for '{o}'.")
                        return
                    manual_stem = entry.get().strip()
                    if not manual_stem:
                        messagebox.showerror("Empty manual name", 
                            f"Manual name for '{o}' cannot be empty.")
                        return
                    suffix = get_texture_suffix(o)
                    new_name = manual_stem + suffix
                    tex_rename_map[o] = new_name
                    
                    # Check length
                    old_base, old_suffix = get_texture_base_and_suffix(o)
                    if len(manual_stem) != len(old_base):
                        length_errors.append(
                            f"'{old_base}' ({len(old_base)})  ->  '{manual_stem}' ({len(manual_stem)})"
                        )
                elif is_auto:
                    # Use auto-derived name
                    if o != n:  # Only add if there's an actual change
                        tex_rename_map[o] = n
                        # Check length
                        old_base, old_suffix = get_texture_base_and_suffix(o)
                        new_base, new_suffix = get_texture_base_and_suffix(n)
                        if len(old_base) != len(new_base):
                            length_errors.append(
                                f"'{old_base}' ({len(old_base)})  ->  '{new_base}' ({len(new_base)})"
                            )
            
            if length_errors:
                messagebox.showerror(
                    "Length mismatch",
                    "Derived texture names must be exactly the same length as originals.\n\n"
                    + "\n".join(length_errors))
                return
            
            # Build texture path map from the stored texture_data
            if "texture_data" in item:
                for old_full_path, old_filename in item["texture_data"]:
                    if old_filename in tex_rename_map:
                        new_filename = tex_rename_map[old_filename]
                        # Replace the old filename in the path with the new one
                        new_full_path = old_full_path.replace(old_filename, new_filename)
                        texture_path_map[old_filename] = (old_full_path, new_full_path)
        else:
            tex_rename_map = {}

        # ── Graph file length warning (skipped in gear swap mode — any length is fine)
        new_mg_name = new_mg_stem + src.suffix
        if not gear_swap and len(new_mg_stem) != len(old_mg_stem):
            diff = len(new_mg_stem) - len(old_mg_stem)
            sign = "+" if diff > 0 else ""
            themed_error(self,
                "Graph File Name Length Mismatch",
                f"The new graph filename must be exactly the same length as the original.\n\n"
                f"  Old:  {old_mg_stem}  ({len(old_mg_stem)} chars)\n"
                f"  New:  {new_mg_stem}  ({len(new_mg_stem)} chars)  [{sign}{diff}]\n\n"
                f"Adjust the name so both are the same length.")
            return

        # ── Determine output paths — all files go to MOD FOLDER ROOT (not source folder)
        dst_mgraph = mod_folder / new_mg_name

        # Texture copies only relevant when not in gear swap mode
        tex_copies = []  # (src_path, dst_path, old_name, new_name, folder_path)
        if not gear_swap:
            for old_name, new_name in tex_rename_map.items():
                tex_src = self._texture_results.get(old_name)
                if tex_src is None:
                    continue  # not found on disk, skip
                
                # Get the folder structure from the mgraph file
                if old_name in texture_path_map:
                    _, new_full_path = texture_path_map[old_name]
                    # Extract folder path from the full path
                    folder_path = "/".join(new_full_path.split("/")[:-1])
                    # Create full destination path preserving folder structure
                    dst_tex = mod_folder / folder_path.replace("/", os.sep) / new_name
                else:
                    # Fallback: if no path info, just put in mod folder root
                    dst_tex = mod_folder / new_name
                    folder_path = ""
                
                tex_copies.append((tex_src, dst_tex, old_name, new_name, folder_path))

        # ── Overwrite check
        all_dsts = [dst_mgraph] + [d for _, d, _, _, _ in tex_copies]
        existing = [d for d in all_dsts if d.exists()]
        if existing:
            names = "\n".join(f"  {p.name}" for p in existing)
            if not messagebox.askyesno(
                "Overwrite?",
                f"These files already exist in the mod folder:\n\n{names}\n\nOverwrite?"):
                return

        # ── Read the .mgraphobject binary
        try:
            mg_data = src.read_bytes()
        except Exception as e:
            messagebox.showerror("Read error", f"Could not read {src.name}:\n{e}")
            return

        # ── Patch internal DDS refs only when not in gear swap mode
        # We need to patch FULL PATHS in the binary, not just filenames
        if not gear_swap:
            try:
                for old_name in tex_rename_map.keys():
                    if old_name in texture_path_map:
                        old_full_path, new_full_path = texture_path_map[old_name]
                        mg_data = patch_binary_string(mg_data, old_full_path, new_full_path)
            except ValueError as e:
                messagebox.showerror("Patch error", str(e))
                return

        # ── Execute all writes
        ok, failed = [], []

        # Write (patched) + renamed .mgraphobject to MOD FOLDER
        try:
            dst_mgraph.parent.mkdir(parents=True, exist_ok=True)
            dst_mgraph.write_bytes(mg_data)
            ok.append(dst_mgraph.name)
        except Exception as e:
            failed.append(f"{dst_mgraph.name}: {e}")

        # Copy textures (renamed) to MOD FOLDER with folder structure — skipped in gear swap mode
        for tex_src, tex_dst, old_name, new_name, folder_path in tex_copies:
            try:
                tex_dst.parent.mkdir(parents=True, exist_ok=True)
                shutil.copy2(tex_src, tex_dst)
                if folder_path:
                    ok.append(f"{old_name} -> {folder_path}/{new_name}")
                else:
                    ok.append(f"{old_name} -> {new_name}")
            except Exception as e:
                failed.append(f"{old_name}: {e}")

        if ok and not failed:
            self._set_status(
                f"Done -- {len(ok)} file(s) written to mod folder"
                + (" (gear swap only)." if gear_swap else " with renamed textures."),
                color=SUCCESS)
        elif ok:
            self._set_status(f"Done with errors -- {len(failed)} failed.", color=WARN)
            messagebox.showwarning("Partial success", "Failed:\n" + "\n".join(failed))
        else:
            self._set_status("Copy failed.", color=ERROR)
            messagebox.showerror("Copy failed", "\n".join(failed))

        self._scan_mod(mod)

    # ── Undo/Redo ──────────────────────────────────────────────────────────────────
    
    def _undo(self, event=None):
        """Undo the last change to the graph name entry."""
        if not self._undo_stack:
            return "break"  # Prevent default behavior
        
        # Push current value to redo stack
        current = self._new_mg_var.get()
        self._redo_stack.append(current)
        
        # Pop from undo stack and set
        previous = self._undo_stack.pop()
        self._undo_in_progress = True
        self._new_mg_var.set(previous)
        self._last_value = previous
        self._undo_in_progress = False
        
        return "break"  # Prevent default Tkinter undo
    
    def _redo(self, event=None):
        """Redo the last undone change to the graph name entry."""
        if not self._redo_stack:
            return "break"  # Prevent default behavior
        
        # Push current value to undo stack
        current = self._new_mg_var.get()
        self._undo_stack.append(current)
        
        # Pop from redo stack and set
        next_value = self._redo_stack.pop()
        self._undo_in_progress = True
        self._new_mg_var.set(next_value)
        self._last_value = next_value
        self._undo_in_progress = False
        
        return "break"  # Prevent default Tkinter redo

