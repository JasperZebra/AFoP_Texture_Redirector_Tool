class FileList(tk.Frame):
    """Searchable, filterable list of .mgraphobject files."""

    def __init__(self, parent, stem_index, on_select=None, **kw):
        super().__init__(parent, bg=BG, **kw)
        self._stem_index = stem_index
        self._on_select  = on_select
        self._all_items  = []
        self._filtered   = []

        # Search bar
        sf = tk.Frame(self, bg=ENTRY_BG, highlightbackground=BORDER, highlightthickness=1)
        sf.pack(fill="x", pady=(0, 5))
        tk.Label(sf, text="⌕", bg=ENTRY_BG, fg=MUTED,
                 font=("Segoe UI", 11)).pack(side="left", padx=(7, 2))
        self._sv = tk.StringVar()
        self._sv.trace_add("write", lambda *_: self._refresh())
        tk.Entry(sf, textvariable=self._sv, bg=ENTRY_BG, fg=TEXT,
                 insertbackground=ACCENT, relief="flat",
                 font=("Segoe UI", 9), bd=0
                 ).pack(side="left", fill="x", expand=True, pady=6, padx=(0, 6))

        # Slot filter + count
        fr = tk.Frame(self, bg=BG)
        fr.pack(fill="x", pady=(0, 5))
        tk.Label(fr, text="Slot:", bg=BG, fg=MUTED, font=("Segoe UI", 9)).pack(side="left")
        self._slot_var = tk.StringVar(value="ALL SLOTS")
        self._slot_cb  = ttk.Combobox(fr, textvariable=self._slot_var,
                                       values=["ALL SLOTS"], state="readonly",
                                       width=12, font=("Segoe UI", 9))
        self._slot_cb.pack(side="left", padx=(5, 0))
        self._slot_cb.bind("<<ComboboxSelected>>", lambda _: self._refresh())
        self._count_var = tk.StringVar()
        tk.Label(fr, textvariable=self._count_var, bg=BG, fg=MUTED,
                 font=("Segoe UI", 8)).pack(side="right")

        # Package filter
        fr2 = tk.Frame(self, bg=BG)
        fr2.pack(fill="x", pady=(0, 5))
        tk.Label(fr2, text="Package:", bg=BG, fg=MUTED, font=("Segoe UI", 9)).pack(side="left")
        self._pkg_var = tk.StringVar(value="ALL PACKAGES")
        self._pkg_cb  = ttk.Combobox(fr2, textvariable=self._pkg_var,
                                      values=["ALL PACKAGES"], state="readonly",
                                      width=14, font=("Segoe UI", 9))
        self._pkg_cb.pack(side="left", padx=(5, 0))
        self._pkg_cb.bind("<<ComboboxSelected>>", lambda _: self._refresh())

        # Text widget used as a coloured listbox (same approach as swap tool)
        wrap = tk.Frame(self, bg=BORDER, highlightbackground=BORDER, highlightthickness=1)
        wrap.pack(fill="both", expand=True)
        sb = tk.Scrollbar(wrap, bg=PANEL, troughcolor=BG, activebackground=ACCENT)
        sb.pack(side="right", fill="y")
        self._lb = tk.Text(wrap, bg=ENTRY_BG, fg=TEXT,
                           relief="flat", bd=0, cursor="arrow",
                           font=("Segoe UI", 9), yscrollcommand=sb.set,
                           highlightthickness=0, state="disabled",
                           spacing1=2, spacing3=2)
        self._lb.pack(side="left", fill="both", expand=True)
        sb.config(command=self._lb.yview)
        self._sel_idx = None
        self._lb.bind("<Button-1>", self._on_click)
        self._lb.bind("<Button-3>", self._on_right_click)
        self._lb.bind("<MouseWheel>", lambda e: self._lb.yview_scroll(int(-1*(e.delta/120)), "units"))

        # Right-click context menu
        self._ctx_menu = tk.Menu(self, tearoff=0, bg=PANEL, fg=TEXT,
                                  activebackground=BTN_BG, activeforeground=ACCENT,
                                  font=("Segoe UI", 9), relief="flat", bd=1)

    def load(self, paths: list):
        slots_seen = set()
        pkgs_seen  = set()
        items = []
        for p in sorted(paths, key=lambda x: x.stem.lower()):
            stem  = p.stem.lower()
            entry = self._stem_index.get(stem, {})
            slot  = entry.get("slot", "Custom").upper()
            pkg   = entry.get("package", "custom")
            slots_seen.add(slot)
            pkgs_seen.add(pkg)
            items.append({
                "path":         p,
                "stem":         p.stem,
                "filename":     p.name,
                "slot":         slot,
                "package":      pkg,
                "ui_names":     entry.get("ui_names", []),
                "textures":     [],
                "models":       entry.get("models", []),
                "mgraphobject": entry.get("mgraphobject", []),
            })
        self._all_items = items
        slots = ["ALL SLOTS"] + sorted(s.upper() for s in slots_seen)
        self._slot_cb.config(values=slots)
        self._slot_var.set("ALL SLOTS")
        pkgs = ["ALL PACKAGES"] + sorted(pkgs_seen)
        self._pkg_cb.config(values=pkgs)
        self._pkg_var.set("ALL PACKAGES")
        self._refresh()

    def _refresh(self):
        q    = self._sv.get().strip().lower()
        slot = self._slot_var.get()
        pkg  = self._pkg_var.get()
        self._filtered = [
            i for i in self._all_items
            if (slot == "ALL SLOTS" or i["slot"] == slot)
            and (pkg == "ALL PACKAGES" or i["package"] == pkg)
            and (not q
                 or q in i["stem"].lower()
                 or any(q in n.lower() for n in i["ui_names"]))
        ]
        self._sel_idx = None
        self._lb.config(state="normal")
        self._lb.delete("1.0", "end")

        for item in self._filtered:
            t     = item["slot"].lower()
            p     = item["package"]
            t_tag = f"type_{t}"
            p_tag = f"pkg_{p}"
            self._lb.tag_config(t_tag, foreground=TYPE_COLORS.get(t, TYPE_COLOR_DEFAULT),
                                font=("Segoe UI", 9, "bold"))
            self._lb.tag_config(p_tag, foreground=PKG_COLORS.get(p, PKG_COLOR_DEFAULT),
                                font=("Segoe UI", 9, "bold"))
            self._lb.tag_config("name", foreground=TEXT, font=("Segoe UI", 9))
            self._lb.tag_config("custom_tag", foreground=WARN, font=("Segoe UI", 9))
            self._lb.insert("end", f"[{item['slot'].upper()}]", t_tag)
            self._lb.insert("end", "  ")
            self._lb.insert("end", f"[{p}]", p_tag)
            self._lb.insert("end", "  ")
            if item["ui_names"]:
                self._lb.insert("end", item["ui_names"][0] + "  ", "name")
                self._lb.insert("end", f"({item['stem']})\n", "custom_tag")
            else:
                self._lb.insert("end", f"({item['stem']})\n", "custom_tag")

        self._lb.config(state="disabled")
        self._count_var.set(f"{len(self._filtered)} files")

    def _on_click(self, event):
        index    = self._lb.index(f"@{event.x},{event.y}")
        line_num = int(index.split(".")[0]) - 1  # 0-based
        if 0 <= line_num < len(self._filtered):
            self._sel_idx = line_num
            self._highlight_selected()
            if self._on_select:
                self._on_select(self._filtered[line_num])

    def _highlight_selected(self):
        self._lb.tag_delete("selected_row")
        if self._sel_idx is not None:
            line = self._sel_idx + 1
            self._lb.tag_add("selected_row", f"{line}.0", f"{line}.end")
            self._lb.tag_config("selected_row", background=BTN_BG)

    def _on_right_click(self, event):
        index    = self._lb.index(f"@{event.x},{event.y}")
        line_num = int(index.split(".")[0]) - 1
        if not (0 <= line_num < len(self._filtered)):
            return
        item = self._filtered[line_num]
        self._ctx_menu.delete(0, "end")
        self._ctx_menu.add_command(
            label=f"Copy stem:  {item['stem']}",
            command=lambda: self._copy_filename(item["stem"]))
        try:
            self._ctx_menu.tk_popup(event.x_root, event.y_root)
        finally:
            self._ctx_menu.grab_release()

    def _copy_filename(self, name):
        self.clipboard_clear()
        self.clipboard_append(name)
        self.update()

    def get_selected(self):
        if self._sel_idx is not None and self._sel_idx < len(self._filtered):
            return self._filtered[self._sel_idx]
        return None

    def clear(self):
        self._all_items = []
        self._filtered  = []
        self._sel_idx   = None
        self._lb.config(state="normal")
        self._lb.delete("1.0", "end")
        self._lb.config(state="disabled")
        self._count_var.set("")


class InfoCard(tk.Frame):
    """Detail panel for a selected .mgraphobject entry."""

    def __init__(self, parent, title, title_color, **kw):
        super().__init__(parent, bg=PANEL, highlightbackground=BORDER,
                         highlightthickness=1, **kw)
        hdr = tk.Frame(self, bg=PANEL)
        hdr.pack(fill="x", padx=10, pady=(8, 4))
        tk.Label(hdr, text=title, bg=PANEL, fg=title_color,
                 font=("Segoe UI", 9, "bold")).pack(side="left")
        self._slot_lbl = tk.Label(hdr, text="", bg=PANEL, fg=MUTED,
                                   font=("Segoe UI", 8))
        self._slot_lbl.pack(side="right")
        tk.Frame(self, bg=BORDER, height=1).pack(fill="x")
        self._text = tk.Text(self, bg=PANEL, fg=TEXT, relief="flat",
                              font=(MONO, 10), bd=0, state="disabled",
                              wrap="word", padx=10, pady=8,
                              highlightthickness=0, width=1, height=1)
        self._text.pack(fill="both", expand=True)
        self._text.tag_config("key",     foreground=MUTED)
        self._text.tag_config("val",     foreground=TEXT)
        self._text.tag_config("acc",     foreground=ACCENT,  font=(MONO, 10, "bold"))
        self._text.tag_config("acc2",    foreground=ACCENT2)
        self._text.tag_config("warn",    foreground=WARN)
        self._text.tag_config("found",   foreground=SUCCESS)
        self._text.tag_config("missing", foreground=ERROR)
        self._text.tag_config("muted",   foreground=MUTED,   font=(MONO, 9))
        self.clear()

    def clear(self):
        self._slot_lbl.config(text="")
        self._write([("key", "No file selected.")])

    def show(self, item: dict, texture_results: dict = None):
        pkg = item.get("package", "")
        self._slot_lbl.config(text=f"{item['slot']}  ·  {pkg}" if pkg else item["slot"])
        lines = [("key", "File:\n"), ("acc", f"  {item['filename']}\n")]
        if item["ui_names"]:
            lines += [("key", "\nKnown as:\n")]
            for n in item["ui_names"]:
                lines += [("val", f"  {n}\n")]
        else:
            lines += [("key", "\n"), ("warn", "  Custom file - not in gear index.\n")]

        tex_source = item.get("tex_source")  # "binary", "default", or None (scanning)

        if texture_results is None:
            # Still scanning
            lines += [("key", "\n"), ("warn", "  Scanning for textures...\n")]
        elif not item["textures"]:
            lines += [("key", "\n"), ("muted", "  No textures found.\n")]
        else:
            if tex_source == "binary":
                lines += [("key", "\nTextures  "), ("muted", "(read from graph file):\n")]
            elif tex_source == "default":
                lines += [("key", "\nTextures  "), ("muted", "(guessed from filename):\n")]
            else:
                lines += [("key", "\nTextures:\n")]

            for t in item["textures"]:
                if texture_results.get(t):
                    lines += [("found", f"  {t}\n")]
                else:
                    lines += [("missing", f"  {t}\n")]

            found = sum(1 for v in texture_results.values() if v)
            total = len(texture_results)
            tag   = "found" if found == total else ("warn" if found else "missing")
            lines += [("key", "\n"), (tag, f"  {found}/{total} found\n")]

        self._write(lines)

    def _write(self, segs):
        self._text.config(state="normal")
