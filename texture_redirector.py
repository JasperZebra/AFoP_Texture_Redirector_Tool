    # ── Stem change / validation ───────────────────────────────────────────────

    def _derive_texture_names(self, new_mg_stem: str) -> list:
        """
        Given a new graph file stem, derive new texture filenames.

        Strategy:
        - Strip gender suffixes (_f, _m) from both graph stems to get the "core" name
        - For each texture, check if it starts with the old graph core
        - If related: replace the matching prefix with new graph core, keep remainder
          (e.g., 'g_res_torso_base_02a' with '02' -> '03' becomes 'g_res_torso_base_03a')
        - If unrelated (like dn_cotton01_n.dds): keep unchanged
        
        This properly handles cases where:
          - Graph: g_res_torso_base_02_f -> g_res_torso_base_03_f
          - Texture: g_res_torso_base_02a_d.dds -> g_res_torso_base_03a_d.dds
        
        Returns list of (old_tex_name, new_tex_name) for all textures.
        """
        item = self._sel_res
        if not item:
            return []
        old_mg_stem  = item["path"].stem
        all_textures = item.get("textures", [])
        if not all_textures:
            return []

        # Strip gender suffix from graph stems (_f, _m) for comparison
        old_mg_core = old_mg_stem.rstrip('_fm')
        if old_mg_core and old_mg_core[-1] == '_':
            old_mg_core = old_mg_core[:-1]
        
        new_mg_core = new_mg_stem.rstrip('_fm')
        if new_mg_core and new_mg_core[-1] == '_':
            new_mg_core = new_mg_core[:-1]

        pairs = []
        for t in all_textures:
            suffix = get_texture_suffix(t)

            if not suffix:
                # Not a targeted texture type — always keep as-is
                pairs.append((t, t, False))
                continue

            tex_stem = t[:-len(suffix)]

            # Check if texture stem starts with the old graph core
            if tex_stem.lower().startswith(old_mg_core.lower()):
                # Texture is related - replace the matching prefix with the new one
                remainder = tex_stem[len(old_mg_core):]  # Keep suffixes like 'a', 'b', '01', etc.
                new_tex_stem = new_mg_core + remainder
                new_tex_name = new_tex_stem + suffix
                is_linked = True
            else:
                # Unrelated / generic texture (like dn_cotton01_n.dds) - keep unchanged
                new_tex_name = t
                is_linked = False

            pairs.append((t, new_tex_name, is_linked))
        return pairs

    def _on_mg_name_change(self, *_):
        """Live-validate new graph name and rebuild read-only derived texture rows."""
        new_mg = self._new_mg_var.get().strip()
        item   = self._sel_res
        
        # Track undo/redo history (skip if we're doing undo/redo to avoid recursion)
        if not self._undo_in_progress:
            current_value = self._new_mg_var.get()
            if current_value != self._last_value:
                self._undo_stack.append(self._last_value)
                self._last_value = current_value
                self._redo_stack.clear()  # Clear redo stack on new changes

        # Strip extension if the user typed it in
        if new_mg.lower().endswith(".mgraphobject"):
            new_mg = new_mg[: -len(".mgraphobject")]

        # Clear existing rows and checkbox state
        for w in self._tex_check_frame.winfo_children():
            w.destroy()
        self._tex_check_vars = {}   # old_tex_name -> BooleanVar (auto-derive from graph name)
        self._tex_manual_vars = {}  # old_tex_name -> BooleanVar (manual naming)
        self._tex_manual_entries = {}  # old_tex_name -> Entry widget
        self._tex_manual_names = {}  # old_tex_name -> StringVar (manual name storage)

        if not item or not new_mg:
            self._mg_len_lbl.config(text="")
            self._tex_len_lbl.config(text="")
            self._slot_warn_lbl.config(text="")
            return

        old_mg    = item["path"].stem
        gear_swap = self._gear_swap_only.get()

        # ── Slot mismatch warning
        new_entry = self._stem_index.get(new_mg.lower())
        if new_mg.lower() != old_mg.lower() and new_entry and new_entry.get("slot") and new_entry["slot"] != item["slot"]:
            self._slot_warn_lbl.config(
                text=f"⚠  Slot mismatch: source is '{item['slot']}', target is '{new_entry['slot']}' — may not display correctly or might crash in-game.")
        else:
            self._slot_warn_lbl.config(text="")

        if gear_swap:
            self._mg_len_lbl.config(text=f"{len(new_mg)} chars  ✓", fg=SUCCESS)
            self._tex_len_lbl.config(text="skipped", fg=MUTED)
            tk.Label(self._tex_check_frame,
                     text="  (gear swap only — DDS files unchanged)",
                     bg=PANEL, fg=MUTED, font=("Segoe UI", 8)).pack(anchor="w")
            return

        # ── Graph name length indicator
        diff = len(new_mg) - len(old_mg)
        sign = "+" if diff > 0 else ""
        self._mg_len_lbl.config(
            text=f"{len(new_mg)} chars  ({sign}{diff})" if diff != 0 else f"{len(new_mg)} chars  ✓",
            fg=ERROR if diff != 0 else SUCCESS)

        pairs = self._derive_texture_names(new_mg)

        # ── Table layout for texture rows using grid
        # Columns: 0=linked_cb, 1=edit_cb, 2=stem_entry, 3=suffix, 4=diff_badge
        COL_CB1    = 0
        COL_CB2    = 1
        COL_STEM   = 2
        COL_SUFFIX = 3
        COL_DIFF   = 4

        HIGHLIGHT_BG = "#2a3550"

        if pairs:
            # Header row
            tk.Label(self._tex_check_frame, text="linked", bg=PANEL, fg=MUTED,
                     font=("Segoe UI", 7)).grid(row=0, column=COL_CB1, sticky="w", padx=(2,0), pady=(0,2))
            tk.Label(self._tex_check_frame, text="edit", bg=PANEL, fg=MUTED,
                     font=("Segoe UI", 7)).grid(row=0, column=COL_CB2, sticky="w", padx=(2,0), pady=(0,2))
            tk.Label(self._tex_check_frame, text="texture name", bg=PANEL, fg=MUTED,
                     font=("Segoe UI", 7)).grid(row=0, column=COL_STEM, sticky="w", padx=(4,0), pady=(0,2))
            tk.Label(self._tex_check_frame, text="suffix", bg=PANEL, fg=MUTED,
                     font=("Segoe UI", 7)).grid(row=0, column=COL_SUFFIX, sticky="w", padx=(2,0), pady=(0,2))
            # Make stem column expandable
            self._tex_check_frame.columnconfigure(COL_STEM, weight=1)

        for row_idx, (old_t, new_t, is_linked) in enumerate(pairs, start=1):
            suffix    = get_texture_suffix(old_t)
            unchanged = (old_t == new_t)
            has_err   = bool(suffix) and (len(new_t) - len(suffix) != len(old_t) - len(suffix))
            new_stem  = new_t[: -len(suffix)] if suffix else new_t
            old_stem  = old_t[: -len(suffix)] if suffix else old_t

            was_unlinked = old_t in self._tex_unlinked
            check_var = tk.BooleanVar(value=is_linked and not unchanged and not was_unlinked)
            manual_var = tk.BooleanVar(value=False)
            manual_name_var = tk.StringVar(value=old_stem if (unchanged or was_unlinked) else new_stem)

            self._tex_check_vars[old_t] = check_var
            self._tex_manual_vars[old_t] = manual_var
            self._tex_manual_names[old_t] = manual_name_var

            # Stem colour: error=red, ok=accent2, unchanged/unlinked=muted
            stem_fg = MUTED if (unchanged or was_unlinked) else (ERROR if has_err else ACCENT2)
            init_bg = HIGHLIGHT_BG if (is_linked and not unchanged and not was_unlinked) else PANEL

            # ── First checkbox
            def _make_auto_toggle(cv, mv, stem_entry, on_fg, new_stem_text, old_stem_text, diff_lbl, tex_name):
                def _toggle():
                    if cv.get():
                        self._tex_unlinked.discard(tex_name)
                        mv.set(False)
                        stem_entry.config(state="normal")
                        stem_entry.delete(0, tk.END)
                        stem_entry.insert(0, new_stem_text)
                        stem_entry.config(state="readonly", readonlybackground=HIGHLIGHT_BG, fg=on_fg)
                        if diff_lbl:
                            diff_lbl.grid()
                    else:
                        self._tex_unlinked.add(tex_name)
                        stem_entry.config(state="normal")
                        stem_entry.delete(0, tk.END)
                        stem_entry.insert(0, old_stem_text)
                        stem_entry.config(state="readonly", fg=MUTED, readonlybackground=PANEL)
                        if diff_lbl:
                            diff_lbl.grid_remove()
                    self._update_tex_len_lbl()
                return _toggle

            def _make_manual_toggle(cv, mv, stem_entry, manual_name_var_inner, on_fg):
                def _toggle():
                    if mv.get():
                        cv.set(False)
                        stem_entry.config(state="normal", bg=HIGHLIGHT_BG, fg=on_fg)
                        stem_entry.focus_set()
                        stem_entry.icursor(tk.END)
                        manual_name_var_inner.set(stem_entry.get())
                    else:
                        stem_entry.config(state="readonly", readonlybackground=PANEL, bg=PANEL, fg=MUTED)
                    self._update_tex_len_lbl()
                return _toggle

            # Only show the linked checkbox for textures related to the mgraph
            if is_linked:
                cb_auto = tk.Checkbutton(self._tex_check_frame, variable=check_var,
                                    bg=PANEL, fg=MUTED, selectcolor=ENTRY_BG,
                                    activebackground=PANEL, activeforeground=TEXT,
                                    relief="flat", bd=0, highlightthickness=0, cursor="hand2")
                cb_auto.grid(row=row_idx, column=COL_CB1, sticky="w", padx=(2,0), pady=1)
            else:
                # Placeholder so grid column stays aligned
                tk.Label(self._tex_check_frame, bg=PANEL, width=2).grid(
                    row=row_idx, column=COL_CB1, sticky="w", padx=(2,0), pady=1)

            cb_manual = tk.Checkbutton(self._tex_check_frame, variable=manual_var,
                                bg=PANEL, fg=MUTED, selectcolor=ENTRY_BG,
                                activebackground=PANEL, activeforeground=TEXT,
                                relief="flat", bd=0, highlightthickness=0, cursor="hand2")
            cb_manual.grid(row=row_idx, column=COL_CB2, sticky="w", padx=(2,0), pady=1)

            stem_entry = tk.Entry(self._tex_check_frame, textvariable=manual_name_var,
                                 bg=PANEL, fg=stem_fg, font=(MONO, 8),
                                 relief="flat", bd=2, state="readonly",
                                 width=28, insertbackground=TEXT,
                                 readonlybackground=init_bg, disabledbackground=PANEL)
            stem_entry.grid(row=row_idx, column=COL_STEM, sticky="ew", padx=(4,0), pady=1)
            self._tex_manual_entries[old_t] = stem_entry

            manual_name_var.trace_add("write", lambda *args, ot=old_t: self._validate_manual_name(ot))

            suffix_lbl = tk.Label(self._tex_check_frame, text=suffix, bg=PANEL, fg=MUTED,
                                  font=(MONO, 8), anchor="w")
            suffix_lbl.grid(row=row_idx, column=COL_SUFFIX, sticky="w", padx=(2,8), pady=1)

            old_stem_len = len(old_t) - len(suffix)
            new_stem_len = len(new_t) - len(suffix)
            diff_lbl = None
            if new_stem_len != old_stem_len:
                tdiff = new_stem_len - old_stem_len
                tsign = "+" if tdiff > 0 else ""
                diff_lbl = tk.Label(self._tex_check_frame, text=f"{tsign}{tdiff}", bg=PANEL, fg=ERROR,
                                    font=(MONO, 8))
                diff_lbl.grid(row=row_idx, column=COL_DIFF, sticky="w", padx=(0,4), pady=1)
                if was_unlinked:
                    diff_lbl.grid_remove()

            if is_linked:
                cb_auto.config(command=_make_auto_toggle(check_var, manual_var, stem_entry, stem_fg, new_stem, old_stem, diff_lbl, old_t))
            cb_manual.config(command=_make_manual_toggle(check_var, manual_var, stem_entry, manual_name_var, stem_fg))

        # Bind mousewheel to every child widget so scrolling works anywhere in the section
        def _bind_mousewheel(widget):
            widget.bind("<MouseWheel>", lambda e: self._tex_check_frame.master.yview_scroll(
                int(-1 * (e.delta / 120)), "units"))
            for child in widget.winfo_children():
                _bind_mousewheel(child)
        _bind_mousewheel(self._tex_check_frame)

